export function getAllCalonId() {
  const calons = getCalonList();

  const calonId = calons.map(function (calon) {
    return calon.nama;
  });

  return calonId.map((calonId) => {
    return {
      params: {
        id: calonId,
      },
    };
  });
}

export function getCalonDetail(id) {
  const allcalon = getCalonList();
  const calon = allcalon.findIndex((calon) => calon.name == id);



  return {
    id,
    calon
  }
}
